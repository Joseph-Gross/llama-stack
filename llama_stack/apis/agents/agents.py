@runtime_checkable
@trace_protocol
class Agents(Protocol):
    """Agents API for creating and interacting with agentic systems.

    Main functionalities provided by this API:
    - Create agents with specific instructions and ability to use tools.
    - Interactions with agents are grouped into sessions ("threads"), and each interaction is called a "turn".
    - Agents can be provided with various tools (see the ToolGroups and ToolRuntime APIs for more details).
    - Agents can be provided with various shields (see the Safety API for more details).
    - Agents can also use Memory to retrieve information from knowledge bases. See the RAG Tool and Vector IO APIs for more details.
    
    This API serves as the primary interface for creating and managing agents,
    their sessions, turns, and steps in the Llama Stack framework.
    """

    @webmethod(route="/agents", method="POST")
    async def create_agent(
        self,
        agent_config: AgentConfig,
    ) -> AgentCreateResponse:
        """Create a new agent with the specified configuration.
        
        This method creates a new agent with the provided configuration,
        including model, instructions, and optional tools.
        
        Args:
            agent_config: Configuration for the new agent
            
        Returns:
            Response containing the ID of the newly created agent
        """
        ...

    @webmethod(route="/agents/{agent_id}/session/{session_id}/turn", method="POST")
    async def create_agent_turn(
        self,
        agent_id: str,
        session_id: str,
        messages: List[
            Union[
                UserMessage,
                ToolResponseMessage,
            ]
        ],
        stream: Optional[bool] = False,
        documents: Optional[List[Document]] = None,
        toolgroups: Optional[List[Any]] = None,  # List[AgentToolGroup]
        tool_config: Optional[ToolConfig] = None,
        allow_turn_resume: Optional[bool] = False,
    ) -> Union[Turn, AsyncIterator[AgentTurnResponseStreamChunk]]:
        """Create a new turn in an agent session.
        
        This method creates a new turn in the specified agent session,
        processing the provided messages and returning the agent's response.
        
        Args:
            agent_id: Identifier for the agent
            session_id: Identifier for the session
            messages: List of messages to process in this turn
            stream: Whether to stream the response
            documents: Optional list of documents to provide to the agent
            toolgroups: Optional list of tool groups to override the agent's default tools
            tool_config: Optional tool configuration to override the agent's default
            allow_turn_resume: Whether to allow resuming this turn
            
        Returns:
            Either a complete Turn object or an AsyncIterator of stream chunks
        """
        ...

    @webmethod(
        route="/agents/{agent_id}/session/{session_id}/turn/{turn_id}/resume",
        method="POST",
    )
    async def resume_agent_turn(
        self,
        agent_id: str,
        session_id: str,
        turn_id: str,
        tool_responses: List[ToolResponseMessage],
        stream: Optional[bool] = False,
    ) -> Union[Turn, AsyncIterator[AgentTurnResponseStreamChunk]]:
        """Resume an agent turn with executed tool call responses.
        
        When a Turn has the status `awaiting_input` due to pending input from client side 
        tool calls, this endpoint can be used to submit the outputs from the tool calls 
        once they are ready.
        
        Args:
            agent_id: The ID of the agent to resume
            session_id: The ID of the session to resume
            turn_id: The ID of the turn to resume
            tool_responses: The tool call responses to resume the turn with
            stream: Whether to stream the response
            
        Returns:
            A Turn object if stream is False, otherwise an AsyncIterator of 
            AgentTurnResponseStreamChunk objects
        """
        ...

    @webmethod(
        route="/agents/{agent_id}/session/{session_id}/turn/{turn_id}",
        method="GET",
    )
    async def get_agents_turn(
        self,
        agent_id: str,
        session_id: str,
        turn_id: str,
    ) -> Turn:
        """Retrieve a specific turn from an agent session.
        
        This method retrieves the details of a specific turn in an agent session,
        including all steps and messages.
        
        Args:
            agent_id: Identifier for the agent
            session_id: Identifier for the session
            turn_id: Identifier for the turn to retrieve
            
        Returns:
            The complete Turn object with all details
        """
        ...

    @webmethod(
        route="/agents/{agent_id}/session/{session_id}/turn/{turn_id}/step/{step_id}",
        method="GET",
    )
    async def get_agents_step(
        self,
        agent_id: str,
        session_id: str,
        turn_id: str,
        step_id: str,
    ) -> AgentStepResponse:
        """Retrieve a specific step from an agent turn.
        
        This method retrieves the details of a specific step within a turn,
        such as an inference step, tool execution step, etc.
        
        Args:
            agent_id: Identifier for the agent
            session_id: Identifier for the session
            turn_id: Identifier for the turn
            step_id: Identifier for the step to retrieve
            
        Returns:
            Response containing the step details
        """
        ...

    @webmethod(route="/agents/{agent_id}/session", method="POST")
    async def create_agent_session(
        self,
        agent_id: str,
        session_name: str,
    ) -> AgentSessionCreateResponse:
        """Create a new session for an agent.
        
        This method creates a new session for the specified agent,
        which can be used to group related interactions.
        
        Args:
            agent_id: Identifier for the agent
            session_name: Name for the new session
            
        Returns:
            Response containing the ID of the newly created session
        """
        ...

    @webmethod(route="/agents/{agent_id}/session/{session_id}", method="GET")
    async def get_agents_session(
        self,
        session_id: str,
        agent_id: str,
        turn_ids: Optional[List[str]] = None,
    ) -> Session:
        """Retrieve a specific agent session.
        
        This method retrieves the details of a specific agent session,
        optionally including only the specified turns.
        
        Args:
            session_id: Identifier for the session to retrieve
            agent_id: Identifier for the agent
            turn_ids: Optional list of turn IDs to include in the response
            
        Returns:
            The complete Session object with requested turns
        """
        ...

    @webmethod(route="/agents/{agent_id}/session/{session_id}", method="DELETE")
    async def delete_agents_session(
        self,
        session_id: str,
        agent_id: str,
    ) -> None:
        """Delete a specific agent session.
        
        This method permanently deletes an agent session and all its associated turns and steps.
        
        Args:
            session_id: Identifier for the session to delete
            agent_id: Identifier for the agent
            
        Returns:
            None
        """
        ...

    @webmethod(route="/agents/{agent_id}", method="DELETE")
    async def delete_agent(
        self,
        agent_id: str,
    ) -> None:
        """Delete a specific agent.
        
        This method permanently deletes an agent and all its associated sessions, turns, and steps.
        
        Args:
            agent_id: Identifier for the agent to delete
            
        Returns:
            None
        """
        ...
