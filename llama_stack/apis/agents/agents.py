class AgentTurnResponseEventType(Enum):
    """Enumeration of event types for agent turn responses.
    
    These event types represent different stages and actions in the agent turn
    response lifecycle.
    
    Attributes:
        step_start: Event indicating the start of a step
        step_complete: Event indicating the completion of a step
        step_progress: Event indicating progress within a step
        turn_start: Event indicating the start of a turn
        turn_complete: Event indicating the completion of a turn
        turn_awaiting_input: Event indicating the turn is waiting for input
    """
    step_start = "step_start"
    step_complete = "step_complete"
    step_progress = "step_progress"

    turn_start = "turn_start"
    turn_complete = "turn_complete"
    turn_awaiting_input = "turn_awaiting_input"


