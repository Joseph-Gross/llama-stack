@runtime_checkable
@trace_protocol
class Agents(Protocol):
    """Agents API for creating and interacting with agentic systems.

    This protocol defines the interface for creating and interacting with agents,
    which are AI systems that can perform tasks, use tools, and maintain state
    across multiple interactions.

    Main functionalities provided by this API:
    - Create agents with specific instructions and ability to use tools.
    - Interactions with agents are grouped into sessions ("threads"), and each interaction is called a "turn".
    - Agents can be provided with various tools (see the ToolGroups and ToolRuntime APIs for more details).
    - Agents can be provided with various shields (see the Safety API for more details).
    - Agents can also use Memory to retrieve information from knowledge bases. See the RAG Tool and Vector IO APIs for more details.
    """
    # The following methods are protocol definitions and not actual implementations

    # @webmethod(route="/agents", method="POST")
    async def create_agent(
        self,
        agent_config: AgentConfig,
    ) -> AgentCreateResponse:
        """Create a new agent with the specified configuration.
        
        Args:
            agent_config: Configuration for the agent, including model, instructions,
                          and available tools.
                          
        Returns:
            A response containing the ID of the created agent.
        """
        ...

    # @webmethod(route="/agents/{agent_id}/session/{session_id}/turn", method="POST")
    async def create_agent_turn(
        self,
        agent_id: str,
        session_id: str,
        messages: List[
            Union[
                UserMessage,
                ToolResponseMessage,
            ]
        ],
        stream: Optional[bool] = False,
        documents: Optional[List[Document]] = None,
        toolgroups: Optional[List[Any]] = None,  # List[AgentToolGroup]
        tool_config: Optional[ToolConfig] = None,
        allow_turn_resume: Optional[bool] = False,
    ) -> Union[Turn, AsyncIterator[AgentTurnResponseStreamChunk]]:
        """Create a new turn in an agent session.
        
        A turn represents a single interaction with the agent, where the agent
        receives messages, processes them, and generates a response. The agent
        may use tools, retrieve information from memory, and apply safety shields
        during this process.
        
        Args:
            agent_id: The ID of the agent to interact with.
            session_id: The ID of the session to create the turn in.
            messages: The messages to send to the agent.
            stream: Whether to stream the response or return it all at once.
            documents: Optional documents to provide to the agent.
            toolgroups: Optional tool groups to make available for this turn.
            tool_config: Optional tool configuration for this turn.
            allow_turn_resume: Whether to allow resuming this turn later.
            
        Returns:
            If stream is False, returns a Turn object containing the complete
            interaction. If stream is True, returns an AsyncIterator that yields
            AgentTurnResponseStreamChunk objects as the turn progresses.
        """
        ...

    # Protocol method for resuming an agent turn
    # route="/agents/{agent_id}/session/{session_id}/turn/{turn_id}/resume", method="POST"
    async def resume_agent_turn(
        self,
        agent_id: str,
        session_id: str,
        turn_id: str,
        tool_responses: List[ToolResponseMessage],
        stream: Optional[bool] = False,
    ) -> Union[Turn, AsyncIterator[AgentTurnResponseStreamChunk]]:
        """Resume an agent turn with executed tool call responses.

        When a Turn has the status `awaiting_input` due to pending input from client side tool calls, this endpoint can be used to submit the outputs from the tool calls once they are ready.

        :param agent_id: The ID of the agent to resume.
        :param session_id: The ID of the session to resume.
        :param turn_id: The ID of the turn to resume.
        :param tool_responses: The tool call responses to resume the turn with.
        :param stream: Whether to stream the response.
        :returns: A Turn object if stream is False, otherwise an AsyncIterator of AgentTurnResponseStreamChunk objects.
        """
        ...

