class AgentConfigCommon(BaseModel):
    """Common configuration parameters for agents.
    
    This class defines the common configuration parameters that are shared
    between different agent configuration classes.
    
    Attributes:
        sampling_params: Parameters for controlling the sampling behavior of the model
        input_shields: List of input safety shields to apply
        output_shields: List of output safety shields to apply
        toolgroups: List of tool groups available to the agent
        client_tools: List of client-side tools available to the agent
        tool_choice: Tool choice configuration (deprecated, use tool_config instead)
        tool_prompt_format: Tool prompt format configuration (deprecated, use tool_config instead)
        tool_config: Consolidated tool configuration
        max_infer_iters: Maximum number of inference iterations
    """
    sampling_params: Optional[SamplingParams] = SamplingParams()

    input_shields: Optional[List[str]] = Field(default_factory=list)
    output_shields: Optional[List[str]] = Field(default_factory=list)
    toolgroups: Optional[List[Any]] = Field(default_factory=list)  # List[AgentToolGroup]
    client_tools: Optional[List[ToolDef]] = Field(default_factory=list)
    tool_choice: Optional[ToolChoice] = Field(default=None, deprecated="use tool_config instead")
    tool_prompt_format: Optional[ToolPromptFormat] = Field(default=None, deprecated="use tool_config instead")
    tool_config: Optional[ToolConfig] = Field(default=None)

    max_infer_iters: Optional[int] = 10

    def model_post_init(self, __context):
        """Post-initialization validation and setup.
        
        This method is called after the model is initialized to validate
        configuration parameters and set up defaults.
        
        Args:
            __context: Initialization context (not used)
            
        Raises:
            ValueError: If deprecated parameters are used inconsistently with tool_config
        """
        if self.tool_config:
            if self.tool_choice and self.tool_config.tool_choice != self.tool_choice:
                raise ValueError("tool_choice is deprecated. Use tool_choice in tool_config instead.")
            if self.tool_prompt_format and self.tool_config.tool_prompt_format != self.tool_prompt_format:
                raise ValueError("tool_prompt_format is deprecated. Use tool_prompt_format in tool_config instead.")
        else:
            params = {}
            if self.tool_choice:
                params["tool_choice"] = self.tool_choice
            if self.tool_prompt_format:
                params["tool_prompt_format"] = self.tool_prompt_format
            self.tool_config = ToolConfig(**params)


