class StepCommon(BaseModel):
    """Common attributes for all step types in an agent interaction.
    
    Attributes:
        turn_id: Unique identifier for the turn this step belongs to.
        step_id: Unique identifier for this step.
        started_at: Timestamp when the step started, or None if not started.
        completed_at: Timestamp when the step completed, or None if not completed.
    """
    turn_id: str
    step_id: str
    started_at: Optional[datetime] = None
    completed_at: Optional[datetime] = None


class StepType(Enum):
    """Types of steps that can occur during an agent interaction.
    
    Attributes:
        inference: A step where the agent generates a response.
        tool_execution: A step where the agent executes a tool.
        shield_call: A step where a safety shield is called.
        memory_retrieval: A step where the agent retrieves information from memory.
    """
    inference = "inference"
    tool_execution = "tool_execution"
    shield_call = "shield_call"
    memory_retrieval = "memory_retrieval"


@json_schema_type
class InferenceStep(StepCommon):
    """A step where the agent generates a response using an inference model.
    
    Attributes:
        step_type: The type of step, always "inference" for this class.
        model_response: The response generated by the model.
    """
    model_config = ConfigDict(protected_namespaces=())

    step_type: str = StepType.inference.value
    model_response: CompletionMessage


@json_schema_type
class ToolExecutionStep(StepCommon):
    """A step where the agent executes one or more tools.
    
    Attributes:
        step_type: The type of step, always "tool_execution" for this class.
        tool_calls: List of tool calls made by the agent.
        tool_responses: List of responses from the executed tools.
    """
    step_type: str = StepType.tool_execution.value
    tool_calls: List[ToolCall]
    tool_responses: List[ToolResponse]


@json_schema_type
class ShieldCallStep(StepCommon):
    """A step where a safety shield is called to check for violations.
    
    Attributes:
        step_type: The type of step, always "shield_call" for this class.
        violation: The safety violation detected, if any.
    """
    step_type: str = StepType.shield_call.value
    violation: Optional[SafetyViolation]


@json_schema_type
class MemoryRetrievalStep(StepCommon):
    """A step where the agent retrieves information from memory.
    
    Attributes:
        step_type: The type of step, always "memory_retrieval" for this class.
        vector_db_ids: Identifier for the vector database used for retrieval.
        inserted_context: The context retrieved from memory.
    """
    step_type: str = StepType.memory_retrieval.value
    vector_db_ids: str
    inserted_context: Any  # InterleavedContent


